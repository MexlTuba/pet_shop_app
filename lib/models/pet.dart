class Pet {
  final String id;
  final String name;
  final String breed;
  final double price;
  final String location;
  final String distance;
  final double weight;
  final double height;
  final String color;
  final String imageUrl;

  Pet({
    required this.id,
    required this.name,
    required this.breed,
    required this.price,
    required this.location,
    required this.distance,
    required this.weight,
    required this.height,
    required this.color,
    required this.imageUrl,
  });
}
